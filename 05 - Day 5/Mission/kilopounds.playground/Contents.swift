// Kilopunds
// The formula is: Pounds = Kilograms * 2.205
// var pounds: Double = 0
var kilograms: Double = 10

// pounds = kilograms * 2.205
func kilopunds(k: Double) -> Double {
    return k * 2.205
}

print(kilopunds(k: 10))
print(kilopunds(k: 100))
print(kilopunds(k: 111.78))
print(kilopunds(k: 9.9))
print(kilopunds(k: 2))
print(kilopunds(k: 1.9))
